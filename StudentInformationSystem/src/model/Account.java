package model;

public class Account {
	int accountNo;
	double balance;
	public int getAccountNo() {
		return accountNo;
	}
	public double getBalance() {
		return balance;
	}
	public void setAccountNo(int accountNo) {
		this.accountNo = accountNo;
	}
	public void setBalance(double balance) {
		this.balance = balance;
	}
	
}
